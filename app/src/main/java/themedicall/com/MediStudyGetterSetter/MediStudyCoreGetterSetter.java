public class MediStudyCoreGetterSetter {
    private String id;
    private String name;

    public MediStudyCoreGetterSetter(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
